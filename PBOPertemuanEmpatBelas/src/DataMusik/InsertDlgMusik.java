    private void loadAlbum() {
        try {
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();

            // Ambil seluruh objek Album, bukan hanya id-nya
            Query q = em.createQuery("SELECT a FROM Album a ORDER BY a.namaAlbum");
            List<Album> list = q.getResultList();

            jCBoxNama.removeAllItems();
            jCBoxNama.addItem("Pilih Nama Album");

            for (Album a : list) {
                jCBoxNama.addItem(a.getNamaAlbum());  // tampilkan nama album
            }

            em.close();
            emf.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal mengambil data album: " + e.getMessage());
        }
    }

