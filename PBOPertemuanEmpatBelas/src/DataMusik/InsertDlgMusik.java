    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (tfIdLagu.getText().equals("") || jCBoxNama.getSelectedIndex() == 0
                || tfJudul.getText().equals("") || tfArtis.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu!");
            return;
        }

        String idMusik = tfIdLagu.getText();
        String namaAlbumTerpilih = jCBoxNama.getSelectedItem().toString();
        String judul = tfJudul.getText();
        String artis = tfArtis.getText();

        try {

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            // ðŸ”¹ Ambil entitas Album berdasarkan nama
            Query qAlbum = em.createQuery("SELECT a FROM Album a WHERE a.namaAlbum = :nama");
            qAlbum.setParameter("nama", namaAlbumTerpilih);

            Album album;
            try {
                album = (Album) qAlbum.getSingleResult();
            } catch (NoResultException e) {
                JOptionPane.showMessageDialog(null, "Album tidak ditemukan di database!",
                        "Error", JOptionPane.ERROR_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // ðŸ”¹ Validasi 1 â€” ID Musik (Primary Key) tidak boleh duplikat
            Musik existing = em.find(Musik.class, idMusik);
            if (existing != null) {
                JOptionPane.showMessageDialog(null,
                        "ID Musik sudah digunakan! Gunakan ID lain.",
                        "Duplikat ID Musik", JOptionPane.WARNING_MESSAGE);
                em.close();
                emf.close();
                return;
            }

            // ðŸ”¹ Validasi 2 â€” judul + artis + album sama â†’ tidak boleh duplikat
            Query qCheck = em.createQuery(
                    "SELECT COUNT(m) FROM Musik m "
                    + "WHERE m.judul = :judul AND m.artis = :artis AND m.idAlbum = :album"
            );
            qCheck.setParameter("judul", judul);
            qCheck.setParameter("artis", artis);
            qCheck.setParameter("album", album);
            long count = (long) qCheck.getSingleResult();

            if (count > 0) {
                JOptionPane.showMessageDialog(null,
                        "Lagu dengan judul & artis ini sudah ada dalam album tersebut!",
                        "Duplikat Lagu", JOptionPane.WARNING_MESSAGE);
                em.close();
                emf.close();
                return;
            }


            // ðŸ”¹ Simpan data
            Musik musik = new Musik();
            musik.setIdMusik(idMusik);
            musik.setIdAlbum(album);   // FK ke entitas Album
            musik.setJudul(judul);
            musik.setArtis(artis);

            em.persist(musik);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(null, "Data musik berhasil disimpan!");

            // Bersihkan form
            bersih();

            // Refresh tabel di JFrame Utama
            if (utama != null) {
                utama.showTableMusik();
            }

            em.close();
            emf.close();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null,
                    "Terjadi kesalahan saat menyimpan data: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            e.printStackTrace();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

