    private void btnUploadMusikActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUploadMusikActionPerformed
        JFileChooser jfc = new JFileChooser(FileSystemView.getFileSystemView().getHomeDirectory());
        int returnValue = jfc.showOpenDialog(null);

        if (returnValue == JFileChooser.APPROVE_OPTION) {
            File filePilihan = jfc.getSelectedFile();
            System.out.println("yang dipilih : " + filePilihan.getAbsolutePath());

            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();

            int jumlahBerhasil = 0;
            int jumlahDuplikat = 0;
            int jumlahAlbumTidakAda = 0;
            int jumlahKosong = 0;

            try (BufferedReader br = new BufferedReader(new FileReader(filePilihan))) {
                String baris;
                String pemisah = ";";

                em.getTransaction().begin();

                while ((baris = br.readLine()) != null) {
                    String[] data = baris.split(pemisah);

                    // Format: idMusik ; namaAlbum ; judul ; artis
                    if (data.length < 4) {
                        jumlahKosong++;
                        continue;
                    }

                    String idMusik = data[0].trim();
                    String namaAlbum = data[1].trim();
                    String judul = data[2].trim();
                    String artis = data[3].trim();

                    // Cek data kosong
                    if (idMusik.isEmpty() || namaAlbum.isEmpty()
                            || judul.isEmpty() || artis.isEmpty()) {
                        jumlahKosong++;
                        continue;
                    }

                    // Cek duplikat ID Musik
                    Musik cek = em.find(Musik.class, idMusik);
                    if (cek != null) {
                        jumlahDuplikat++;
                        continue;
                    }

                    // Cek album berdasarkan namaAlbum
                    Album album;
                    try {
                        Query qAlbum = em.createQuery("SELECT a FROM Album a WHERE a.namaAlbum = :nama");
                        qAlbum.setParameter("nama", namaAlbum);
                        album = (Album) qAlbum.getSingleResult();
                    } catch (NoResultException e) {
                        jumlahAlbumTidakAda++;
                        continue;
                    }

                    // Insert Musik
                    Musik m = new Musik();
                    m.setIdMusik(idMusik);
                    m.setJudul(judul);
                    m.setArtis(artis);
                    m.setIdAlbum(album);

                    em.persist(m);
                    jumlahBerhasil++;
                }

                em.getTransaction().commit();
                showTableMusik();

                JOptionPane.showMessageDialog(null,
                        "UPLOAD MUSIK SELESAI!\n"
                        + "Berhasil: " + jumlahBerhasil + "\n"
                        + "Duplikat ID Musik: " + jumlahDuplikat + "\n"
                        + "Album Tidak Ada: " + jumlahAlbumTidakAda + "\n"
                        + "Data Kosong/Tidak Lengkap: " + jumlahKosong,
                        "Hasil Upload Musik",
                        JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception ex) {
                if (em.getTransaction().isActive()) {
                    em.getTransaction().rollback();
                }
                Logger.getLogger(Utama.class.getName()).log(Level.SEVERE, null, ex);
                JOptionPane.showMessageDialog(null, "Terjadi kesalahan saat upload musik");
            } finally {
                em.close();
                emf.close();
            }
        }
    }//GEN-LAST:event_btnUploadMusikActionPerformed

