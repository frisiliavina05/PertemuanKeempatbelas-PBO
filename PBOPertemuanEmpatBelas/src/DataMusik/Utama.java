    public void showTableAlbum() {
        EntityManager em = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU").createEntityManager();
        Query q = em.createNamedQuery("Album.findAll");
        List<Album> hasil = q.getResultList();

        DefaultTableModel modelAlbum = new DefaultTableModel(new String[]{"ID Album", "Nama Album", "Genre", "Tahun Rilis"}, 0);
        for (Album a : hasil) {
            modelAlbum.addRow(new Object[]{a.getIdAlbum(), a.getNamaAlbum(), a.getGenre(), a.getTahunRilis()});
        }
        jTblAlbum.setModel(modelAlbum);
    }

    public void showTableMusik() {
        EntityManager em = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU").createEntityManager();
        Query q = em.createNamedQuery("Musik.findAll");
        List<Musik> hasil = q.getResultList();

        DefaultTableModel modelMusik = new DefaultTableModel(
                new String[]{"ID Lagu", "Nama Album", "Judul", "Artis"}, 0);

        for (Musik m : hasil) {

            // ambil data dari relasi Album (FK)
            String idAlbum = (m.getIdAlbum() != null) ? m.getIdAlbum().getNamaAlbum() : "";

            modelMusik.addRow(new Object[]{
                m.getIdMusik(),
                m.getIdAlbum().getNamaAlbum(),
                m.getJudul(), // kolom ke-3 = Judul
                m.getArtis(), // kolom ke-4 = Artis
            });
        }

        jTblMusik.setModel(modelMusik);
        em.close();
    }

