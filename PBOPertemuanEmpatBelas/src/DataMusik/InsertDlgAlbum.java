    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if (tfID.getText().equals("") || tfNama.getText().equals("")
                || tfGenre.getText().equals("") || tfTahun.getText().equals("")) {

            JOptionPane.showMessageDialog(null, "Isi semua data terlebih dahulu!");
        } else {
            String id, nama, genre;
            id = tfID.getText();
            nama = tfNama.getText();
            genre = tfGenre.getText();
            int tahun;

            try {
                tahun = Integer.parseInt(tfTahun.getText());
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Tahun harus berupa angka!", "Error", JOptionPane.ERROR_MESSAGE);
                return;
            }

            // Buat koneksi persistence
            EntityManagerFactory emf = Persistence.createEntityManagerFactory("PBOPertemuanDuabelasPU");
            EntityManager em = emf.createEntityManager();
            em.getTransaction().begin();

            // ðŸ”Ž Cek apakah ID sudah ada di database
            Album existing = em.find(Album.class, id);
            if (existing != null) {
                JOptionPane.showMessageDialog(null, "ID Album sudah ada! Gunakan ID lain.", "Duplikasi ID", JOptionPane.ERROR_MESSAGE);
                em.close();
                emf.close();
                return; // stop proses
            }

            // Cek apakah judul film sudah ada
            Query q = em.createQuery("SELECT COUNT(a) FROM Album a WHERE a.namaAlbum = :namaAlbum");
            q.setParameter("namaAlbum", nama);
            long count = (long) q.getSingleResult();

            if (count > 0) {
                JOptionPane.showMessageDialog(null, "Nama album sudah ada!", "Error", JOptionPane.ERROR_MESSAGE);
                em.getTransaction().rollback();
                return;
            }
            

            // Buat object Album
            Album a = new Album();
            a.setIdAlbum(id);
            a.setNamaAlbum(nama);
            a.setGenre(genre);
            a.setTahunRilis(tahun);

            // Simpan ke database
            em.persist(a);
            em.getTransaction().commit();

            JOptionPane.showMessageDialog(null, "Data Album berhasil disimpan!");

            // Bersihkan field input
            bersih();

            // Refresh tabel di JFrame (pastikan ada method showTable() di JFrame)
            utama.showTableAlbum();

            em.close();
            emf.close();
        }
    }//GEN-LAST:event_btnSaveActionPerformed

